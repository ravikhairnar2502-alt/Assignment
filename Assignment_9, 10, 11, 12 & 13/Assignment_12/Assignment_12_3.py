# 3. Write a program which accepts two numbers and prints addition, subtraction, multiplication and division.

def main():

    No1 = int(input("Enter first number : "))
    No2 = int(input("Enter second number : "))

    print("Addition is : ",No1 + No2)
    print("Subtraction is : ",No1 - No2)
    print("Multiplication is : ",No1 * No2)
    print("Division is : ",No1 / No2)


if __name__ == "__main__":
    main()



